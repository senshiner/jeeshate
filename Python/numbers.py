a = int(input("Masukkan angka pertama: "))
b = int(input("Masukkan angka kedua: "))

print("Penjumlahan:", a + b) # plus
print("Pengurangan:", a - b) # minus
print("Perkalian:", a * b) # kali
print("Pembagian:", a / b) # bagi (float)
print("Pembagian bulat:", a // b) # bagi (int)
print("Sisa bagi:", a % b) # modulus
print("Pangkat:", a ** b) # pangkat

# Prioritas : () » ** » *, /, //, % » +, -
# Urutan dari kiri ke kanan